pub const CHAR_W: usize = 6;
pub const CHAR_H: usize = 8;

#[derive(Clone, Copy)]
pub struct TextStyle {
    pub scale: u8, // 1 = 6x8, 2 = 12x16, 3 = 18x24
    pub bold: bool,
    pub italic: bool,
}

impl TextStyle {
    pub const fn normal() -> Self {
        TextStyle {
            scale: 1,
            bold: false,
            italic: false,
        }
    }
    pub const fn bold() -> Self {
        TextStyle {
            scale: 1,
            bold: true,
            italic: false,
        }
    }
    pub const fn italic() -> Self {
        TextStyle {
            scale: 1,
            bold: false,
            italic: true,
        }
    }
    pub const fn large() -> Self {
        TextStyle {
            scale: 2,
            bold: false,
            italic: false,
        }
    }
    pub const fn large_bold() -> Self {
        TextStyle {
            scale: 2,
            bold: true,
            italic: false,
        }
    }
}

/// Get the 6 column bytes for a character, with style applied
pub fn get_char_columns(c: char, style: TextStyle) -> [u8; 6] {
    let idx = c as usize;
    let mut cols = if idx >= 32 && idx <= 126 {
        FONT_6X8[idx - 32]
    } else {
        FONT_6X8[0] // unknown chars → space
    };

    // Bold: OR each column with itself shifted right by 1
    // thickens every vertical stroke
    if style.bold {
        for i in 0..5 {
            cols[i] |= cols[i] >> 1;
        }
    }

    // Italic: shift top bits left progressively
    // creates a lean effect across the glyph
    if style.italic {
        cols[0] = (cols[0] as i8 >> 1) as u8; // lean top-left
        cols[1] = cols[1]; // center unchanged
        cols[4] = cols[4] << 1; // lean bottom-right
    }

    cols
}
